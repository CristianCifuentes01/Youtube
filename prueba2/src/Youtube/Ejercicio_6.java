package Youtube;

public class Ejercicio_6 {

	public static void main(String[] args) {
		
		int contador=1;
		System.out.println();
		while(contador<=10) {
			System.out.println(contador);
			contador++;
		}
		
		System.out.println();
		for(int i=1; i<=10 ;i++ ) {
			System.out.println(i);
		}
		
		System.out.println();
		contador=1;
		do {
			System.out.println(contador);
			contador++;
		}while(contador<=10);

	}

}
